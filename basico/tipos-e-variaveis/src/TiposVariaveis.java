public class TiposVariaveis {
  public static void main(String[] args) {
    
    //A estrutura padrão para se declarar uma variável sempre é:
    //<Tipo> <nomeVariavel> <atribuicaoDeValorOpcional>
    int idade; //Tipo "int", nome "idade", com nenhum valor atribuído. 
    int anoFabricacao = 2021; //tipo "int", nome "anoFabricacao", com valor 2021.
    double salarioMinimo = 2500; //tipo "double", nome "salarioMinimo", valor 2.500.
    float pi = 3.14F; // o tipo float precisa do f no final
    String cep = "09-391/420";
    long cpf = 98765432109L; //precisa terminar com L e se começar com zero, talvez tenha que ser outro tipo
    String cpfCorreto = "012.345.678.90"; 

  }  
}
